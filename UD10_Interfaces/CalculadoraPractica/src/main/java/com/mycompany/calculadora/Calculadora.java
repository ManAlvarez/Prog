    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
        // TODO add your handling code here:
        segundoDouble = Double.parseDouble(String.valueOf(pantalla.getText()));
        if (masClick) {
            totalDouble = primerDouble + segundoDouble;
        } else if (menosClick) {
            totalDouble = primerDouble - segundoDouble;
        } else if (multiClick) {
            totalDouble = primerDouble * segundoDouble;
        } else if (divClick) {
            totalDouble = primerDouble / segundoDouble;
        } else {
            totalDouble = primerDouble;
        }

        primerDouble = totalDouble;
        masClick = false;
        menosClick = false;
        multiClick = false;
        divClick = false;
        puntoClick = false;
        pantalla.setText(String.valueOf(totalDouble));

    }//GEN-LAST:event_jButtonIgualActionPerformed

